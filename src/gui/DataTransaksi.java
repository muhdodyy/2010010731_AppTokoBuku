    private void totalTFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalTFActionPerformed
        // TODO add your handling code here:
       if(jbukuTF.getText().equals("") || hargaTF.getText().equals("")){
            //Kasih pesan input tidak boleh kosong
            JOptionPane.showMessageDialog(null, "Input tidak boleh kosong");
        } else {
            int angkaPertama =Integer.valueOf(jbukuTF.getText());
            int angkaKedua =Integer.valueOf(hargaTF.getText());
            int hasil = angkaPertama * angkaKedua;
            hasilTF.setText(Integer.toString(hasil));
            
        }
    }//GEN-LAST:event_totalTFActionPerformed

    private void transaksiTBLMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transaksiTBLMouseClicked
        // TODO add your handling code here:
        int row = transaksiTBL.getSelectedColumn();
        kdpenjualanTF.setText(transaksiTBL.getModel().getValueAt(row, 0).toString());
        kdpelangganTF.setText(transaksiTBL.getModel().getValueAt(row, 1).toString());
        kdbukuTF.setText(transaksiTBL.getModel().getValueAt(row, 2).toString());
        jbukuTF.setText(transaksiTBL.getModel().getValueAt(row, 3).toString());
        hargaTF.setText(transaksiTBL.getModel().getValueAt(row, 4).toString());
        hasilTF.setText(transaksiTBL.getModel().getValueAt(row, 5).toString());
    }//GEN-LAST:event_transaksiTBLMouseClicked

